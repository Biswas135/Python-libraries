# Element-wise arithmetic using NumPy arrays
import numpy as np

# create two 1D NumPy arrays
array1 = np.array([1, 2, 3])
array2 = np.array([4, 5, 6])

# add corresponding elements of the arrays 
print(array1 + array2)  

# subtract corresponding elements 
print(array1 - array2)  

# multiply corresponding elements 
print(array1 * array2)  

# divide corresponding elements
print(array1 / array2)  

# raise elements of array1 to the power of corresponding elements in array2
print(array1 ** array2)  
